            <div class="accordion-content">
                <ul>
                    <li>Les DJ d'anniversaire créent une ambiance joyeuse et énergique pour les fêtes d'anniversaire de tous âges.</li>
                    <li>Ils sélectionnent de la musique populaire et adaptée à l'âge des invités, et peuvent également intégrer des demandes spéciales.</li>
                    <li>Ils peuvent animer des jeux et des concours pour rendre la fête encore plus amusante.</li>
                </ul>
            </div>
        </article>
