                    <li>Les DJ de mariage se spécialisent dans la création d'une ambiance festive et romantique pour les mariages.</li>
                    <li>Ils sélectionnent de la musique adaptée à la cérémonie, au cocktail et à la réception, en tenant compte des goûts du couple et des invités.</li>
                    <li>Ils peuvent également animer des jeux et des activités pour divertir les invités.</li>
                </ul>
