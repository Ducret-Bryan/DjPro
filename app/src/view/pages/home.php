            <div class="accordion-content">
                <ul>
                    <li>Les DJ de soirées thématiques se spécialisent dans la création d'une ambiance en accord avec le thème de l'événement.</li>
                    <li>Que ce soit une soirée des années 80, une soirée disco ou une soirée hawaïenne, ils sélectionnent de la musique et des animations qui correspondent au thème.</li>
                    <li>Ils peuvent également s'habiller et décorer leur installation pour compléter le thème.</li>
                </ul>
            </div>
        </article>
