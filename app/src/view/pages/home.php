            <div class="accordion-content">
                <ul>
                    <li>Les DJ de bar/restaurant animent des soirées dans les bars, les restaurants et les clubs.</li>
                    <li>Ils sélectionnent de la musique qui crée une ambiance conviviale et encourage les clients à rester et à consommer.</li>
                    <li>Ils peuvent également organiser des soirées à thème ou des événements spéciaux.</li>
                </ul>
            </div>
        </article>
