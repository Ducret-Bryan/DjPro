            <div class="accordion-content">
                <p>
                    Le contrat. Nous abordons ici avec vous le critère le plus important. Si vous êtes un particulier nous allons vous expliquer certains détails très important à ne pas négliger dont vous n’avez pas forcément connaissance. Un DJ vous vend une prestation de service et non un bien. Il n’est donc pas commerçant et ne doit en aucun réclamer un acompte mais simplement des arrhes. C’est à dire qu’il ne peut exiger que 30% du montant total de la prestation. Si il vous réclame un acompte non seulement ça n’est pas légal mais en plus vous n’aurez aucun recours si il venait à se désister. Le contrat doit mentionner arrhes versées 30% article 293 du CGI non applicable. Si c’est le cas et que l’animateur DJ vous convient vous pouvez signer. A savoir que le simple fait d’avoir versé des arrhes engage le DJ si il venait à ne pas se présenter. Il vous serait redevable pénalement de deux fois le montant de sa prestation.
                </p>
